from turtle import Turtle
X = 350
Y = 0

class self(Turtle):
    def __init__():
        super().__init__()
        self = Turtle()
        self.penup()
        self.color("white")
        self.shape("square")
        self.goto(350, 0)
        self.penup()
        self.shapesize(stretch_wid=5, stretch_len=1)
        self.goto(X, Y)